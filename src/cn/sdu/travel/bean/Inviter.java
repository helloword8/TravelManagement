package cn.sdu.travel.bean;

//邀请人信息
public class Inviter {
	private String id;
	private String name;
	private String titleCh;
	private String titleEn;
	private String unitName;
	private String address;
	private String telephone;
	private String email;
	private String url;
	private String invitationRaw;
	private String invitationZh;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getTitleCh() {
		return titleCh;
	}

	public void setTitleCh(String titleCh) {
		this.titleCh = titleCh;
	}

	public String getTitleEn() {
		return titleEn;
	}

	public void setTitleEn(String titleEn) {
		this.titleEn = titleEn;
	}

	public String getUnitName() {
		return unitName;
	}

	public void setUnitName(String unitName) {
		this.unitName = unitName;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getTelephone() {
		return telephone;
	}

	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getInvitationRaw() {
		return invitationRaw;
	}

	public void setInvitationRaw(String invitationRaw) {
		this.invitationRaw = invitationRaw;
	}

	public String getInvitationZh() {
		return invitationZh;
	}

	public void setInvitationZh(String invitationZh) {
		this.invitationZh = invitationZh;
	}
}
